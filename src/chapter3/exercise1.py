# A pay computation that gives the employee 1.5times the hourly rate for hours worked above 40hours
hours = int(input("Enter hours worked for; \n"))
rate = 10
hours > 40
# let hours worked above 40 *1.5 be x
X = float((hours - 40) * 1.5)
hours = 40 + X
Pay = (hours * rate)
print("Payment is: \n", Pay)
